"""UI string table and resolver for EasyTer.

English is the default language. A missing key falls back to English, then to
the raw key, so a lookup never raises. The active language is set once at
startup from the config; changing it applies on the next launch."""

_current = "en"


def set_language(lang):
    """Set the active UI language ("en" or "ar"); unknown values fall back to en."""
    global _current
    _current = lang if lang in STRINGS else "en"


def current_language():
    return _current


def t(key, **kwargs):
    """Return the localized string for key in the active language.

    Falls back to English, then to the raw key. Applies str.format(**kwargs)
    when keyword arguments are given."""
    s = STRINGS.get(_current, {}).get(key)
    if s is None:
        s = STRINGS["en"].get(key, key)
    return s.format(**kwargs) if kwargs else s


